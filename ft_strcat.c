char *strcat(char *s1, const char *s2)
{
	char *s1ptr;
	
	s1ptr = s1;
	while (*s1ptr)
		s1ptr++;
	while (*s2)
		*(s1ptr++) = *(s2++);
	*s1ptr = '\0';
	return s1;
}
